    ORG 00H
	AJMP MAIN
	ORG 50H
MAIN:
    MOV A,#07FH   
LOOP:
	MOV P1,A     
	MOV A,P0     
	INC A        
	ACALL DELAY  
	MOV A,P1     
	RR A         
	AJMP LOOP    
DELAY:
	MOV R5,A     
DELAY1: 
	MOV R6,#0FFH
DELAY2:
	MOV R7,#005H
DELAY3:
	DJNZ R7,DELAY3
	DJNZ R6,DELAY2
	DJNZ R5,DELAY1
	RET
	END